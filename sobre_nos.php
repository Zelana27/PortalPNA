				<h2 class="texto">POLÍCIA NACIONAL DE ANGOLA</h2><p></p>
				
					A garantia da ordem, a defesa da segurança e tranquilidade públicas, o asseguramento e protecção das instituições, dos cidadãos 
					e os respectivos bens, contra criminalidade violenta ou organizada e outros tipos de ameaças e riscos, são fundamentos de valor
					sobre a existência da Polícia Nacional de Angola.   Enquanto Órgão de Estado, a Constituição da República estatui no Artigo 210.º
					a Polícia Nacional de Angola como uma instituição nacional, permanente, regular e apartidária, organizada na base da hierarquia 
					e da discíplina, incumbida da protecção e asseguramento policial do País, no estrito respeito pela Constituição e pelas leis, 
					bem como pelas convenções internacionais de que Angola seja parte. A Polícia Nacional é composta exclusivamente por cidadãos 
					angolanos,sendo a sua organização única para todo o território nacional.   Lei própria regula a organização e o funcionamento
					da Polícia Nacional.<p>
							
					<h2 class="texto">Definição e Natureza</h2><p></p>
							
					A Polícia Nacional de Angola nos termos do Artigo 2.º do Decreto Presidencial nº 152/19, de 15 Maio, que aprova o seu Estatuto Orgânico,
					 abreviadamente designada por «PNA» é uma força militarizada, uniformizada e armada, com natureza de força de segurança pública, dotada de 
					 autonomia operacional, administrativa, financeira e patrimonial, e tem por missão fundamental: a) Assegurar e defender a legalidade democrática; b)
					 Garantir a segurança pública e o exercício dos direitos e liberdades fundamentais dos cidadãos; c) Manter a ordem e tranquilidade públicas; d)
					 Colaborar na execução da política de defesa nacional, nos termos da Constituição e da lei. A PNA é constituida por pessoal militarizado e por 
					 pessoal civil.<p>
					 
					<h2 class="texto">Dependência</h2><p></p>

					A PNA, enquanto força de segurança, é dirigida pelo Presidente da República na qualidade de  Comandante-em-Chefe das Forças 
					Armadas Angolanas, e compete ao Departamento Ministerial responsável pela ordem interna e segurança pública ( Ministério do 
					Interior) auxiliar o Presidente da República na condução e direção da PNA.   A PNA, enquanto órgão de segurança e ordem interna, 
					está sujeita à fiscalização política, administrativa e judicial, nos termos da Constituição e da Lei.<p>
					<h2 class="texto">Princípios e Valores da Actividade da PNA</h2><p></p>

					A PNA, no exercício das suas funções, observa especialmente os seguintes princípios: Princípio da Legalidade; Princípio do
					Apartidarismo; Princípio da Proporcionalidade, da Necessidade e da Proibição de Excesso; Princípio da Imparcialidade e da
					Neutralidade; Princípio da Probidade Pública; Princípio da Colaboração com os Particulares; Princípio da Aproximação dos
					Serviços aos Cidadãos; Princípio da Prossecução do Interesse Público; Princípio da Integridade e da  Responsabilidade 
					Solidária; Princípio da Cortesia e da Urbanidade; Princípio da Reserva e da Descrição; Princípio da Lealdade às Instituições
					e Entidades Públicas e aos Superiores Interesses do Estado.<p>
					
					<h2 class="texto">Posicionamento Estratégico da PNA</h2><p></p>

					A PNA, enquanto Órgão da Administração Pública, assume tarefas de índole administrativa, no âmbito da manuntenção da ordem
					interna e da tranquilidade públicas, no asseguramento policial nacional fronteiriço e marítimo, bem como no respeito pelo 
					regular exercício dos direitos fundamentais dos cidadãos. No contexto das suas missões gerais de prevenção e combate à
					criminalidade, a PNA integra ao sistema de segurança nacional, a par das Forças Armadas Angolanas e demais Órgãos e Serviços 
					de segurança e Inteligência de Estado, ao qual permite-lhe colaborar nas políticas de Defesa Nacional, nos termos da
					Constituição e da Lei.<p>
					
					<h2 class="texto">Atribuições Essenciais</h2><p></p>
									
					Em situações de normalidade constitucional, as atribuições da PNA são as constantes no seu Estatuto Orgânico
					(Decreto Presidencial nº 152/19, de 15 de Maio) e demais legislação aplicável, e em situações de excepção, 
					resultantes da legislação sobre a Defesa Nacional e sobre os estados de guerra, de sítio e de emergências.<p>   
								
